public class MatrixVariables {

    private int n;
    private String elem;

    public MatrixVariables(int n, String elem) {
        this.n = n;
        this.elem = elem;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public String getElem() {
        return elem;
    }

    public void setElem(String elem) {
        this.elem = elem;
    }

}
